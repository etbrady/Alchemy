import Foundation
import UIKit

extension UIColor {
    
    class var alchemyBlue: UIColor {
        get {
            return UIColor(red:0.21, green:0.75, blue:0.93, alpha:1.0) // #35BEED
        }
    }
    
    class var alchemyBlack: UIColor {
        get {
            return UIColor(red:0.10, green:0.10, blue:0.10, alpha:1.0) // #191919
        }
    }
    
    class var alchemyDarkGrey: UIColor {
        get {
            return UIColor(red:0.27, green:0.27, blue:0.27, alpha:1.0) // #454545
        }
    }
    
    class var alchemyLightGrey: UIColor {
        get {
            return UIColor(red:0.58, green:0.58, blue:0.58, alpha:1.0) // #949494
        }
    }
}
